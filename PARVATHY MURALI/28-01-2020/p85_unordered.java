
import java.util.HashSet;
import java.util.Set;

public class p85_unordered {

	public static void main(String[] args) {
		Set<String> color=new HashSet<String>();
		color.add("red");
		color.add("blue");
		color.add("yellow");
		color.add("green");
		System.out.println(color);
		for(String x:color)
		{
			System.out.println(color);
		}

	}

}
