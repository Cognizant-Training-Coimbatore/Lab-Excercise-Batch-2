
public class program16_nested_if {

	public static void main(String[] args) {
		int x=5,y=100;
		if(x>0)
		{
			if(y>99)
			{
				System.out.println("true");
			}
			else
			{
				System.out.println("false");
			}
		}
		else
		{
			System.out.println("Nothing executed");
		}
	}

}
