
public class pgm18_nested_else_if {

	public static void main(String[] args)
	{
		int x=-1,y=10;
		if(x>0)
		{
			if(y>99)
			{
				System.out.println("true");
			}
			else
			{
				System.out.println("false");
			}
		}
		else
		{
			System.out.println("nothing executed");
		}

}
}
