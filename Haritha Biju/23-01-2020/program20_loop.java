package demo;

public class program20_loop 
{

	public static void main(String[] args) 
	{
		int i=1;
		
		while(i<10)
		{	System.out.print(i+" ");
			i++;
		}
		System.out.println("\n");
		
		do
		{ System.out.print(i);
	    }while(i<5);
		System.out.println("\n");
		
		for(i=1;i<=10;i++)
			System.out.print(i+" ");
		
		
	}

}
