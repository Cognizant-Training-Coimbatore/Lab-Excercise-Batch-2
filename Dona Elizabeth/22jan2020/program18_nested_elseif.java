
public class program18_nested_elseif {

	public static void main(String[] args) {
		int x=5,y=100;
		if(x>0)
		{
			if(y>95)
			{
				System.out.println("True");
			}
			else
			{
				System.out.println("False");
			}
		}
		else
		{
			System.out.println("Nothing executed....!");
		}
	}

}
